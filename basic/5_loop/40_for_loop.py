#char iteration
for char in "Bangladesh":
    print(char)
    

#array iteration
myList=[11,22,33,'xx','yy',44,55]
for iterator in myList:
    print(iterator)
    
#number iteration
for index in range(10):
    print(index)

#number iteration with start position
for index in range(3,10):
    print(index)
    

#array iteration with length
anotherList=[10,20,30,40,50,'xx','yy','zz']
for index in range(len(anotherList)):
    print(anotherList[index])